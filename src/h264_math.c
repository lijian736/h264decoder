/** the int_log2 implementation is from function ff_log2_c(unsigned int v) at https://github.com/FFmpeg/FFmpeg/blob/master/libavutil/intmath.h */
int int_log2(uint32_t v) {
    int n = 0;
    if (v & 0xffff0000) {
        v >>= 16;
        n += 16;
    }
    if (v & 0xff00) {
        v >>= 8;
        n += 8;
    }
    n += ff_log2_tab[v];

    return n;
}

int int_ceil_log2(uint32_t v) { return int_log2((v - 1) << 1); }

int32_t InverseRasterScan(int32_t index, int32_t sub_width, int32_t sub_height, int32_t width, int32_t is_y) {
    if (is_y) {
        return (index / (width / sub_width)) * sub_height;
    } else {
        return (index % (width / sub_width)) * sub_width;
    }
}